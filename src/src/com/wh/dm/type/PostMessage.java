
package com.wh.dm.type;

public class PostMessage {
    private int no;
    private int id;
    private String title;
    private int mid;
    private String mname;
    private int pid;
    private int temp;
    private int uid;

    public void setId(int _id) {

        id = _id;
    }

    public int getId() {

        return id;
    }

    public void setTitle(String _title) {

        title = _title;
    }

    public String getTitle() {

        return title;
    }

    public void setMid(int _mid) {

        mid = _mid;
    }

    public int getMid() {

        return mid;
    }

    public void setMname(String _mname) {

        mname = _mname;
    }

    public String getMname() {

        return mname;
    }

    public void setPid(int _pid) {

        pid = _pid;
    }

    public int getPid() {

        return pid;
    }

    public void setTemp(int _temp) {

        temp = _temp;
    }

    public int getTemp() {

        return temp;
    }

    public void setUid(int _uid) {

        uid = _uid;
    }

    public int getUid() {

        return uid;
    }
}
