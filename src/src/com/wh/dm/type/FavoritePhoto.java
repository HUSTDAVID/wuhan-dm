package com.wh.dm.type;

public class FavoritePhoto {
	private int no;
	private int aid;
	private String aname;
	private String litpic;
	private String addtime;
	private String sid;
	
	public int getNo(){
		return no;
	}
	
	public void setNo(int _no){
		no=_no;
	}
	
	public int getAid(){
		return aid;
	}
	
	public void setAid(int _aid){
		aid=_aid;
	}
	
	public String getAname(){
		return aname;
	}
	
	public void setAname(String _aname){
		aname=_aname;
	}
	
	public String getLitpic(){
		return litpic;
	}
	
	public void setLitpic(String _litpic){
		litpic=_litpic;
	}
	
	public String getAddtime(){
		return addtime;
	}
	
	public void setAddtime(String _addtime){
		addtime=_addtime;
	}
	
	public String getSid(){
		return sid;
	}
	
	public void setSid(String _sid){
		sid=_sid;
	}
	

}
