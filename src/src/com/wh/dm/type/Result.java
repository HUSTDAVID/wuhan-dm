
package com.wh.dm.type;

public class Result {
    boolean result;
    String msg;

    public void setResult(boolean _result) {

        result = _result;
    }

    public void setMsg(String _msg) {

        msg = _msg;
    }

    public boolean getResult() {

        return result;
    }

    public String getMsg() {

        return msg;
    }

}
