
package com.wh.dm.type;

public class PictureMagazine {

    private int no;
    private int id;
    private int aid;
    private int cover;
    private String description;
    private String pic;
    private String addtime;

    public void setNo(int _no) {

        no = _no;
    }

    public int getNo() {

        return no;
    }

    public void setId(int _id) {

        id = _id;
    }

    public int getId() {

        return id;
    }

    public void setAid(int _aid) {

        aid = _aid;
    }

    public int getAid() {

        return aid;
    }

    public void setCover(int _cover) {

        cover = _cover;
    }

    public int getCover() {

        return cover;
    }

    public void setDescription(String _description) {

        description = _description;
    }

    public String getDescription() {

        return description;
    }

    public void setPic(String _pic) {

        pic = _pic;
    }

    public String getPic() {

        return pic;
    }

    public void setAddtime(String _addtime) {

        addtime = _addtime;
    }

    public String getAddtime() {

        return addtime;
    }
}
