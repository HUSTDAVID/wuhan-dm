
package com.wh.dm.type;

public class TwoMagazine {

    private Magazine leftMagazine;
    private Magazine rightMagazine;

    public Magazine getLeftMagazine() {

        return leftMagazine;
    }

    public void setLeftMagazine(Magazine leftMagazine) {

        this.leftMagazine = leftMagazine;
    }

    public Magazine getRightMagazine() {

        return rightMagazine;
    }

    public void setRightMagazine(Magazine rightMagazine) {

        this.rightMagazine = rightMagazine;
    }

}
